import axios from 'axios';
import * as fs from 'fs';
import * as path from 'path';

interface SimplifyJob {
  id: string;
  title: string;
  company: string;
  location: string;
  url: string;
  posted_date: string;
  description: string;
  commitment?: string; // "Full-time", "Contract", "Consulting"
}

interface ApplicationTracker {
  applications: Application[];
}

interface Application {
  id: string;
  company: string;
  role: string;
  apply_url: string;
  applied_date: string;
  status: 'pending' | 'submitted' | 'interview' | 'offer' | 'rejected';
  cover_letter: string;
  notes: string;
}

const CONFIG_PATH = path.join(__dirname, '../../config.json');
const TRACKER_PATH = path.join(process.env.HOME!, 'Documents/Personal/CLT/MAA/applications.json');

async function scrapeSimplifyJobs(): Promise<SimplifyJob[]> {
  try {
    // Simplify.jobs API endpoint (hypothetical - needs actual API key)
    const response = await axios.get('https://api.simplify.jobs/v1/jobs', {
      params: {
        keywords: 'consulting OR attorney OR coach',
        commitment: 'contract,part-time',
        limit: 50
      },
      headers: {
        'Authorization': `Bearer ${process.env.SIMPLIFY_API_KEY || 'demo-key'}`,
        'User-Agent': 'ReverseRecruiter/1.0'
      }
    });

    return response.data.jobs || [];
  } catch (error) {
    console.error('[ERROR] Simplify.jobs API failed:', error);
    // Fallback to mock data for testing
    return mockJobs();
  }
}

