function filterFor250hRoles(jobs: SimplifyJob[]): SimplifyJob[] {
  const keywords = ['contract', 'consulting', '250', 'part-time', 'attorney', 'coach'];
  return jobs.filter(job => {
    const text = `${job.title} ${job.description} ${job.commitment}`.toLowerCase();
    return keywords.some(kw => text.includes(kw));
  });
}

async function loadOrCreateTracker(): Promise<ApplicationTracker> {
  try {
    if (fs.existsSync(TRACKER_PATH)) {
      const data = fs.readFileSync(TRACKER_PATH, 'utf-8');
      return JSON.parse(data);
    }
  } catch (error) {
    console.warn('[WARN] Could not load tracker, creating new:', error);
  }
  return { applications: [] };
}

async function saveTracker(tracker: ApplicationTracker): Promise<void> {
  const dir = path.dirname(TRACKER_PATH);
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
  }
  fs.writeFileSync(TRACKER_PATH, JSON.stringify(tracker, null, 2));
  console.log(`[INFO] Tracker saved: ${tracker.applications.length} applications`);
}

export async function runScraper(): Promise<Application[]> {
  console.log('[INFO] Starting Simplify.jobs scraper...');
  
  const allJobs = await scrapeSimplifyJobs();
  console.log(`[INFO] Found ${allJobs.length} jobs`);
  
  const filteredJobs = filterFor250hRoles(allJobs);
  console.log(`[INFO] Filtered to ${filteredJobs.length} 250h consulting roles`);
  
  const tracker = await loadOrCreateTracker();
  const newApplications: Application[] = [];
  
  for (const job of filteredJobs) {
    // Check if already applied
    const existing = tracker.applications.find(app => app.company === job.company && app.role === job.title);
    if (existing) {
      console.log(`[SKIP] Already applied: ${job.company} - ${job.title}`);
      continue;
    }
    
    // Create new application entry
    const application: Application = {
      id: `app-${Date.now()}-${Math.random().toString(36).slice(2, 7)}`,
      company: job.company,
      role: job.title,
      apply_url: job.url,
      applied_date: new Date().toISOString(),
      status: 'pending',
      cover_letter: '', // Will be generated by application-writer agent
      notes: `Scraped from Simplify.jobs on ${new Date().toLocaleDateString()}`
    };
    
    newApplications.push(application);
    tracker.applications.push(application);
  }
  
  await saveTracker(tracker);
  console.log(`[OK] Scraped ${newApplications.length} new applications`);
  
  return newApplications;
}

if (require.main === module) {
  runScraper().catch(err => {
    console.error('[FATAL]', err);
    process.exit(1);
  });
}
