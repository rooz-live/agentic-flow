#!/bin/bash
set -euo pipefail
TARGET_PATH="/Volumes/cPanelBackups/execution_bounds/mac_studio"
if [ -d "node_modules" ] && [ ! -L "node_modules" ]; then
    echo "--> [WSJF 9.5] Offloading local node_modules matrix to Sovereign Drive..."
    mkdir -p "$TARGET_PATH"
    NM_TARGET="$TARGET_PATH/node_modules_$(date +%s)"
    mv node_modules "$NM_TARGET" 2>/dev/null || true
    ln -s "$NM_TARGET" node_modules
    echo "✅ Reclaimed node_modules. Capabilities restored via Symlink."
fi
