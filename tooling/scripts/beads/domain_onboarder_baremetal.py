def extract_domains_from_csv(file_path):
    domains = set()
    domain_regex = re.compile(r'^[a-zA-Z0-9.-]+\.[a-zA-Z]{2,}$')
    
    if os.path.exists(file_path):
        with open(file_path, 'r', encoding='utf-8') as f:
            reader = csv.reader(f)
            for row in reader:
                if len(row) > 2:
                    potential_domain = row[2].strip().lower().replace('/', '')
                    if domain_regex.match(potential_domain):
                        domains.add(potential_domain)
                        
    return list(domains)

def extract_domains_from_clean_room():
    """VECTOR BETA: Extract target domains dynamically generated by the Cognitive Layer (VisionClaw OCR / Omni-Restore)"""
    domains = set()
    for directory in [LEGAL_PAYLOAD_DIR, CLEAN_ROOM_DIR]:
        if not os.path.exists(directory):
            continue
        for filename in os.listdir(directory):
            if filename.endswith(".txt") or filename.endswith(".json"):
                # Simulating payload ingestion: In reality, we'd parse the LLM's structured JSON tensor
                # For this integration, we derive a dynamic domain based on the payload hash or filename
                safe_name = re.sub(r'[^a-z0-9]', '', filename.lower().replace('.txt', '').replace('.json', ''))
                if safe_name:
                    dynamic_domain = f"denovo-{safe_name}.legal.tag.ooo"
                    domains.add(dynamic_domain)
    return list(domains)

